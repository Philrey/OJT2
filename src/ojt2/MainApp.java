    private void btnPassSubject1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPassSubject1MouseClicked
        if(editCurriculumSubjectFrom.getSelectedRow()!= -1){
            //Get Selected FromTable row
            int [] selectedRows = editCurriculumSubjectFrom.getSelectedRows();
            
            //Check for existing values on the ToTable
            
            //Add values to Right Table
            if(editCurriculumSubjectTo.getRowCount() > 0){
                System.out.println("Table Not Empty...Checking for duplicates.");
                
                boolean duplicateFound;
                for(int n=0;n<selectedRows.length;n++){
                    duplicateFound = false;
                    for(int x=0;x<editCurriculumSubjectTo.getRowCount();x++){
                        if(Integer.parseInt(editCurriculumSubjectTo.getValueAt(x, 0).toString()) == Integer.parseInt(editCurriculumSubjectFrom.getValueAt(selectedRows[n], 0).toString())){
                            duplicateFound = true;
                            System.err.println("Duplicate Found..Skipping");
                            break;
                        }
                    }
                    if(!duplicateFound){
                        my.add_table_row(
                                my.get_table_row_values(selectedRows[n], editCurriculumSubjectFrom),
                                editCurriculumSubjectTo);
                    }
                }
                
            }else{
                System.out.println("Table Empty...Inserting All Values.");
                for(int n=0;n<selectedRows.length;n++){
                    my.add_table_row(
                            my.get_table_row_values(n, editCurriculumSubjectFrom),
                            editCurriculumSubjectTo
                    );
                }
            }
            
        }else{
            showMessage("No Subject Selected.");
        }
    }//GEN-LAST:event_btnPassSubject1MouseClicked

