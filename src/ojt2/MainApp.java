    private void btnPassSubjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPassSubjectMouseClicked
        if(addCurriculumSubjectFrom.getSelectedRow()!= -1){
            //Get Selected FromTable row
            int [] selectedRows = addCurriculumSubjectFrom.getSelectedRows();
            
            //Check for existing values on the ToTable
            
            //Add values to Right Table
            if(addCurriculumSubjectTo.getRowCount() > 0){
                System.out.println("Table Not Empty...Checking for duplicates.");
                
                boolean duplicateFound;
                for(int n=0;n<selectedRows.length;n++){
                    duplicateFound = false;
                    for(int x=0;x<addCurriculumSubjectTo.getRowCount();x++){
                        if(Integer.parseInt(addCurriculumSubjectTo.getValueAt(x, 0).toString()) == Integer.parseInt(addCurriculumSubjectFrom.getValueAt(selectedRows[n], 0).toString())){
                            duplicateFound = true;
                            System.err.println("Duplicate Found..Skipping");
                            break;
                        }
                    }
                    if(!duplicateFound){
                        my.add_table_row(
                                my.get_table_row_values(selectedRows[n], addCurriculumSubjectFrom),
                                addCurriculumSubjectTo);
                    }
                }
                
            }else{
                System.out.println("Table Empty...Inserting All Values.");
                for(int n=0;n<selectedRows.length;n++){
                    my.add_table_row(
                            my.get_table_row_values(n, addCurriculumSubjectFrom),
                            addCurriculumSubjectTo
                    );
                }
            }
            
        }else{
            showMessage("No Subject Selected.");
        }
    }//GEN-LAST:event_btnPassSubjectMouseClicked

