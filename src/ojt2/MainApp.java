    private void editSaveStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editSaveStudentMouseClicked
        //Get userId
        //Check fields
        //Update database
        
        String toUpdate = editUserIdd.getText();
        String [] fields = {
            "studentId = '"+editStudentId.getText()+"'",
            "firstName = '"+editFirstName.getText()+"'",
            "middleName = '"+editMiddleName.getText()+"'",
            "lastName = '"+editLastName.getText()+"'",
            "birthDate = '"+editBirthDate.getText()+"'",
            "homeAddress = '"+editHomeAddress.getText()+"'",
            "contactNumber = '"+editContactNumber.getText()+"'",
            "gender = '"+btnGender1.getSelectedItem().toString()+"'"
        };
        boolean isComplete = true;
        for(int n=0;n<fields.length;n++){
            if(fields[n].length()<1){
                isComplete = false;
                break;
            }
        }
        
        if(isComplete){
            //Update
            if(my.update_values("students", fields, "userId = "+toUpdate)){
                my.clear_table_rows(studentTable);
                my.refresh_table("*", "students", "", studentTable,my.getStudentOrder());
                editStudentWindow.setVisible(false);
            }else{
                showMessage("Update Failed");
            }
        }else{
            showMessage("Please fill-up all fields");
        }
        
    }//GEN-LAST:event_editSaveStudentMouseClicked

